%% Funciones

%% Funcion para hermanos
hermanode(X,Y) :-
    hijode(X,C),
    hijode(Y,C),
    X \== Y.

%% Funcion para primos
primode(X,Y) :-
    hijode(X,C),
    hijode(Y,D),
    hermanode(C,D),
    X \== Y.

%% Funcion para tios
tiode(X,Y) :-
    hijode(X,C),
    hermanode(C,Y),
    X \== Y.

%% Funcion para sobrinos
sobrinode(X,Y) :-
    hijode(Y,C),
    primode(X,C),
    X \== Y.

%% Funcion para abuelos
abuelode(X,Y) :-
    hijode(C,Y),
    hijode(X,C),
    X \== Y.
%% Funcion para bisabuelos
bisabuelode(X,Y) :-
    hijode(C,Y),
    hijode(D,C),
    hijode(X,D),
    X \== Y.

%% Funcion para bisnietos
bisnietode(X,Y) :-
    abuelode(C,Y),
    hijode(X,C),
    X \== Y.

